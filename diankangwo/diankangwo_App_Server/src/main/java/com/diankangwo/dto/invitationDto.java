package com.diankangwo.dto;

public class invitationDto {

	private String username;
	private String uglify_tel;
	private Double spending_total;
	private Double my_earnings_total;
	private String uid;
	private String gender;
	private String profile_image_url;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUglify_tel() {
		return uglify_tel;
	}
	public void setUglify_tel(String uglify_tel) {
		this.uglify_tel = uglify_tel;
	}
	public double getSpending_total() {
		return spending_total;
	}
	public void setSpending_total(double spending_total) {
		this.spending_total = spending_total;
	}
	public double getMy_earnings_total() {
		return my_earnings_total;
	}
	public void setMy_earnings_total(double my_earnings_total) {
		this.my_earnings_total = my_earnings_total;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getProfile_image_url() {
		return profile_image_url;
	}
	public void setProfile_image_url(String profile_image_url) {
		this.profile_image_url = profile_image_url;
	}

}
